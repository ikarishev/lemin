#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	int i;
	int size;

	if (s && f)
	{
		i = 0;
		size = ft_strlen(s);
		while (i < size)
		{
			f(&s[i++]);
		}
	}
}
